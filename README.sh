## takeSnapshot.sh
- [Oracle VM VirtualBox](https://www.oracle.com/technetwork/jp/server-storage/virtualbox/overview/index.html)で作成したVMのスナップショットを決められた回数分ローテーションしつつ取得するスクリプト
- cronなどの仕込みの種にして、定期的なスナップショットの取得にお役立てください
- 詳細は[こちら](https://red-full-moon.com/virtualbox-snapshots-automatically)も御覧ください

